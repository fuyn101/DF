 % 决策矩阵
decision_matrix = [
1	0.118536197	0.897435897	0	0.090909091
0.368421053	1	0.833333333	0.166666667	0.909090909
1	0.067223548	0.897435897	0.166666667	0.545454545
0.473684211	0.116945107	0	0.5	0.727272727
0	0.000397772	0.948717949	0.166666667	0.363636364
1	0	0.942307692	0.166666667	0.818181818
0.754385965	0.139618138	0.929487179	1	0.909090909
1	0.04176611	1	0.666666667	1
0.438596491	0.566030231	0.935897436	0.666666667	0];


% 计算标准间的相关系数
R = corrcoef(decision_matrix);

% 确定冲突度和信息量
n = size(decision_matrix, 2); % 标准的数量
conflict = zeros(1, n);
for i = 1:n
    conflict(i) = std(decision_matrix(:, i)) * (1 - sum(R(i, :)) / (n - 1));
end

% 计算权重
weights = conflict / sum(conflict);

% 显示结果
disp('指标的权重：');
disp(weights);

% 根据标准化的决策矩阵和权重计算得分
scores = decision_matrix * weights';

% 显示每个分支孔的得分
disp('每个分支孔的得分：');
for i = 1:size(scores, 1)
    fprintf('分支孔%d 的得分: %.2f\n', i, scores(i));
end
