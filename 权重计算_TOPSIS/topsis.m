
% 假设我们有一个数据集 X，其中每一行代表一个样本，每一列代表一种属性
X = [95.64	0.000260845	3.35
    92.36	0.000219135	52
    97.96	0.000504949	8
    94.41	0.000403055	17
    100	0.001158199	3.5
    100	0.000195634	4.5
    100	0.000164752	4.5];

% 样本数和属性数
[n, m] = size(X);

% 正向化，使得所有指标取值都落在 [0, 1] 范围内
for j = 1:m
    X(:, j) = X(:, j) / sqrt(sum(X(:, j).^2));
end

% 指定权重向量 w，使得各个属性按照其重要性被赋予不同的权重
w = [0.114186813 0.073360138 0.209605541];

% 计算加权决策矩阵 W
W = X .* repmat(w, n, 1);

% 确定正理想解 Z 和负理想解 F
Z = max(W, [], 1);
F = min(W, [], 1);

% 计算样本到正理想解和负理想解的距离
D_pos = sqrt(sum((W - repmat(Z, n, 1)).^2, 2));
D_neg = sqrt(sum((W - repmat(F, n, 1)).^2, 2));

% 计算综合评价指数 C
C = D_neg ./ (D_pos + D_neg);

% 将样本按照综合评价指数从大到小排序
[~, rank] = sort(C, 'descend');

% 输出排名最靠前的几个样本
k = 7;
disp(rank(1:k));